# 대입 연산자
e = 10
f = e
g = e + 10 * f
print("e =", e)
print("f =", f)
print("g =", g)

"""
연산자 우선순위
1항 > 2항 > 3항
2항 연산자 중 우선순위
*/ > +-
"""

print("10+3 =", 10+3)
print("10-3 =", 10-3)
print("10*3 =", 10*3)
print("10/3 =", 10/3)
print("10%3 =", 10%3)
print("10**3 =", 10**3)
print("10//3 =", 10//3)